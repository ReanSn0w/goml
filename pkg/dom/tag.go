func Text(value string) view.View {
	return view.External(textValue{content: value})
}

func tag(name string, content []view.View) view.View {
	return view.External(tagValue{name: name, content: view.Group(content...)})
}

type tagValue struct {
	name    string
	content view.View
}

type textValue struct {
	content string
}
